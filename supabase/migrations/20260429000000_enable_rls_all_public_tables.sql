-- Enable Row Level Security on all public-schema tables.
--
-- Supabase flagged `rls_disabled_in_public` on production: tables in the
-- public schema are reachable through the PostgREST REST API with the anon
-- key unless RLS is turned on. Enabling RLS without adding any policies
-- blocks all access through the REST API (anon + authenticated roles).
--
-- Prisma uses the service role key, which bypasses RLS entirely, so
-- application behavior is unaffected.
--
-- Idempotent: `ENABLE ROW LEVEL SECURITY` on an already-enabled table is a
-- no-op.

