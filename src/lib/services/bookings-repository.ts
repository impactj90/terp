/**
 * Bookings Repository
 *
 * Pure Prisma data-access functions for the Booking model.
 * Handles all database queries including list with filters,
 * CRUD operations, derived booking management, and transactional deletes.
 */
import type { PrismaClient } from "@/generated/prisma/client"

// --- Prisma Include Objects ---

const bookingListInclude = {
  bookingType: {
    select: { id: true, code: true, name: true, direction: true },
  },
  bookingReason: {
    select: { id: true, code: true, label: true },
  },
  employee: {
    select: {
      id: true,
      personnelNumber: true,
      firstName: true,
      lastName: true,
      departmentId: true,
    },
  },
} as const

const bookingDetailInclude = {
  ...bookingListInclude,
} as const

// --- Data Scope Types ---

export interface DataScopeFilter {
  type: "all" | "tenant" | "department" | "employee"
  departmentIds: string[]
  employeeIds: string[]
}

// --- Repository Functions ---

export async function findMany(
  prisma: PrismaClient,
  tenantId: string,
  params: {
    page: number
    pageSize: number
    employeeId?: string
    fromDate?: string
    toDate?: string
    bookingTypeId?: string
    source?: string
    dataScope: DataScopeFilter
  }
) {
  const where = buildListWhere(tenantId, params)

  const [items, total] = await Promise.all([
    prisma.booking.findMany({
      where,
      include: bookingListInclude,
      skip: (params.page - 1) * params.pageSize,
      take: params.pageSize,
      orderBy: [{ bookingDate: "desc" }, { editedTime: "desc" }],
    }),
    prisma.booking.count({ where }),
  ])

  return { items, total }
}

export async function findById(
  prisma: PrismaClient,
  tenantId: string,
  id: string
) {
  return prisma.booking.findFirst({
    where: { id, tenantId },
    include: bookingDetailInclude,
  })
}

export async function findByIdWithEmployee(
  prisma: PrismaClient,
  tenantId: string,
  id: string
) {
  return prisma.booking.findFirst({
    where: { id, tenantId },
    include: { employee: { select: { id: true, departmentId: true } } },
  })
}

