// --- Internal Helpers ---

function buildListWhere(
  tenantId: string,
  params: {
    employeeId?: string
    fromDate?: string
    toDate?: string
    bookingTypeId?: string
    source?: string
    dataScope: DataScopeFilter
  }
): Record<string, unknown> {
  const where: Record<string, unknown> = { tenantId }

  if (params.employeeId) {
    where.employeeId = params.employeeId
  }

  if (params.fromDate || params.toDate) {
    const bookingDate: Record<string, unknown> = {}
    if (params.fromDate) {
      bookingDate.gte = new Date(params.fromDate)
    }
    if (params.toDate) {
      bookingDate.lte = new Date(params.toDate)
    }
    where.bookingDate = bookingDate
  }

  if (params.bookingTypeId) {
    where.bookingTypeId = params.bookingTypeId
  }

  if (params.source) {
    where.source = params.source
  }

  // Apply data scope filtering
  const scopeWhere = buildDataScopeWhere(params.dataScope)
  if (scopeWhere) {
    Object.assign(where, scopeWhere)
  }

  return where
}

function buildDataScopeWhere(
  dataScope: DataScopeFilter
): Record<string, unknown> | null {
  if (dataScope.type === "department") {
    return { employee: { departmentId: { in: dataScope.departmentIds } } }
  } else if (dataScope.type === "employee") {
    return { employeeId: { in: dataScope.employeeIds } }
  }
  return null
}
