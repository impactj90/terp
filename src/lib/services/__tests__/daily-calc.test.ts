// --- Mock Prisma Client ---

function createMockPrisma(): {
  prisma: PrismaClient
  mocks: ReturnType<typeof createPrismaMocks>
} {
  const mocks = createPrismaMocks()
  return { prisma: mocks as unknown as PrismaClient, mocks }
}

